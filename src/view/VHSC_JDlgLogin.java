    private void jBtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEntrarActionPerformed
  
        String usuario = jTxtUsuario.getText();
        String senha = new String(jPswSenha.getPassword());

        if (usuario.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
            return;
        }

        Session session = HibernateUtil.getSessionFactory().openSession();

        Query q = session.createQuery(
                "FROM VhscUsuarios WHERE vhscNomeUsuario = :u AND vhscSenha = :s"
        );
        q.setParameter("u", usuario);
        q.setParameter("s", senha);

        VhscUsuarios user = (VhscUsuarios) q.uniqueResult();

        session.close();

        if (user != null) {
            JOptionPane.showMessageDialog(this, "Login efetuado com sucesso!");
            new VHSC_JFrmPrincipal().setVisible(true);
            dispose();
            // fecha a tela e segue para o sistema
        } else {
            JOptionPane.showMessageDialog(this, "Usu√°rio ou senha incorretos!");
        }
    
    }//GEN-LAST:event_jBtnEntrarActionPerformed
                
