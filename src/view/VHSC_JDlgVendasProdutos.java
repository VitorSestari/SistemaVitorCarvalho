    private void VHSC_jCboProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VHSC_jCboProdutosActionPerformed
        // TODO add your handling code here:
        VhscProdutos vhscProdutos = (VhscProdutos) VHSC_jCboProdutos.getSelectedItem();
        VHSC_jTxtPrecoUnitario.setText(VHSC_Util.doubleToStr(vhscProdutos.getVhscPreco()));
        int quant = VHSC_Util.strToInt(VHSC_jTxtQuantidade.getText());
        VHSC_jTxtTotal.setText(VHSC_Util.doubleToStr( quant * vhscProdutos.getVhscPreco()));
    }//GEN-LAST:event_VHSC_jCboProdutosActionPerformed

    private void VHSC_jTxtQuantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VHSC_jTxtQuantidadeActionPerformed
        // TODO add your handling code here:
        if(VHSC_jTxtQuantidade.getText().isEmpty() == false){
        VhscProdutos vhscProdutos = (VhscProdutos) VHSC_jCboProdutos.getSelectedItem();
        int quant = VHSC_Util.strToInt(VHSC_jTxtQuantidade.getText());
        VHSC_jTxtTotal.setText(VHSC_Util.doubleToStr( quant * vhscProdutos.getVhscPreco()));
       } else {
           VHSC_Util.limpar(VHSC_jTxtTotal);
       }
    }//GEN-LAST:event_VHSC_jTxtQuantidadeActionPerformed

